package Mypage;

/* 
 * Notify DTO
 */


public class NotifyDTO {
	private int userNO = 0; // 유저 고유번호
	private int diaryNO = 0; // 일기 고유번호
	private int notifyNO = 0; // 알람 고유번호
	private int pageNO = -1; // 갤러리 페이지 넘버 ( 페이징할 때 사용된다. )
	private int Nonoff = -1; // 알람 켜짐 유무
	private String notifyTitle = ""; // 알람 제목
	private String notifyDate = ""; // 알람 지정 날짜
	private String notifyTime = ""; // 알람 지정 시간
	private String token = "";
	public int getUserNO() {
		return userNO;
	}
	public void setUserNO(int userNO) {
		this.userNO = userNO;
	}
	public int getDiaryNO() {
		return diaryNO;
	}
	public void setDiaryNO(int diaryNO) {
		this.diaryNO = diaryNO;
	}
	public int getNotifyNO() {
		return notifyNO;
	}
	public void setNotifyNO(int notifyNO) {
		this.notifyNO = notifyNO;
	}
	public int getPageNO() {
		return pageNO;
	}
	public void setPageNO(int pageNO) {
		this.pageNO = pageNO;
	}
	public int getNonoff() {
		return Nonoff;
	}
	public void setNonoff(int nonoff) {
		Nonoff = nonoff;
	}
	public String getNotifyTitle() {
		return notifyTitle;
	}
	public void setNotifyTitle(String notifyTitle) {
		this.notifyTitle = notifyTitle;
	}
	public String getNotifyDate() {
		return notifyDate;
	}
	public void setNotifyDate(String notifyDate) {
		this.notifyDate = notifyDate;
	}
	public String getNotifyTime() {
		return notifyTime;
	}
	public void setNotifyTime(String notifyTime) {
		this.notifyTime = notifyTime;
	}
	public String getToken() {
		return token;
	}
	public void setToken(String token) {
		this.token = token;
	}	
}
